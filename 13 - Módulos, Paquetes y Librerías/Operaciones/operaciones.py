def sumar():
    primer_numero=float(input("Introduce un número:\n"))
    segundo_numero=float(input("Introduce otro número:\n"))
    suma_total=primer_numero+segundo_numero

    return suma_total

def restar():
    primer_numero=float(input("Introduce un número:\n"))
    segundo_numero=float(input("Introduce otro número:\n"))
    resta_total=primer_numero-segundo_numero

    return resta_total

def multiplicar():
    primer_numero=float(input("Introduce un número:\n"))
    segundo_numero=float(input("Introduce otro número:\n"))
    multiplicacion_total=primer_numero*segundo_numero

    return multiplicacion_total 
